class RelationalOperation{
	 public static void main(String[] args) {
		int i = 10;
		System.out.println("Content of i" +i);
		int j = 5;
		System.out.println("Content of j"+j);
		boolean c = i > j;
		System.out.println("Is " +i+ " > " +j+ " : " +c );
		boolean d = i < j;
		System.out.println("Is " +i+ " < " +j+ " : " +d );

	}
}