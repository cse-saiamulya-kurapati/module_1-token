class ArithmeticOperation{
	 public static void main(String[] args) {
		int i = 10;
		System.out.println("Content of i" +i);
		int j = 5;
		System.out.println("Content of j" +j);
		int a = i + j;
		System.out.println("Content of a" +a);
		int b = i - j;
		System.out.println("Subtraction of " +i+ " and " +j+ " is : " +b);
		int c = i*j;
		System.out.println("Multiplication of " +i+ " and " +j+ " is : " +c);
		int d = i/j;
	    System.out.println("Division of " +i+ " and " +j+ " is : " +d);

	}
}